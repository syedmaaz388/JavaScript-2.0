//  Arithmetic Operator
console.log(2 + 2);  // Addition
console.log(2 - 4);  // Substraction
console.log(2 * 2); // Multiplication
console.log(2 ** 3); // Exponentiation
console.log(2 / 2); // Division
console.log(2 % 2); // Modulus
let a = 3;
a++;  // Increment
console.log(a);
a--; // Decrement
--a;
console.log(a); 
a++;
console.log(a++);
console.log(a);      
console.log(++a);
console.log(a);


